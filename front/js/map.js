function time_i18n(unit, value)
{
    if (lang == 'en') {
        return unit
    }

    if (lang != 'ru') {
        return unit
    }

    let ru = {
        years:   [ '–≥–æ–¥', '–≥–æ–¥–∞', '–ª–µ—Ç' ],
        days:    [ '–¥–µ–Ω—å', '–¥–Ω—è', '–¥–Ω–µ–π' ],
        hours:   [ '—á–∞—Å', '—á–∞—Å–∞', '—á–∞—Å–æ–≤' ],
        minutes: [ '–º–∏–Ω—É—Ç–∞', '–º–∏–Ω—É—Ç—ã', '–º–∏–Ω—É—Ç' ],
        seconds: [ '—Å–µ–∫—É–Ω–¥–∞', '—Å–µ–∫—É–Ω–¥—ã', '—Å–µ–∫—É–Ω–¥' ],
    }

    let ru_units = ru[unit]

    if (value > 10 && value < 20) {
        return ru_units[2];
    }

    let last = Math.ceil(value) % 10

    if (last == 1) {
        return ru_units[0]
    }

    if (last >=2 && last < 5) {
        return ru_units[1]
    }

    return ru_units[2]
}


function forHumans (seconds) {

    var numyears = Math.floor(seconds / 31536000);
    var numdays = Math.floor((seconds % 31536000) / 86400);
    var numhours = Math.floor(((seconds % 31536000) % 86400) / 3600);
    var numminutes = Math.floor((((seconds % 31536000) % 86400) % 3600) / 60);
    var numseconds = (((seconds % 31536000) % 86400) % 3600) % 60;

    let   out = [ numyears, numdays, numhours, numminutes, numseconds ];
    let names = [ "years",  "days",  "hours",  "minutes",  "seconds" ];

    let outs = []

    for (let i = 0; i < out.length; i++) {
        if (out[i] != 0) {
            outs[i] = Math.ceil(out[i]) + ' ' + time_i18n(names[i], out[i])
        }
    }

    return outs.join(' ')
}

